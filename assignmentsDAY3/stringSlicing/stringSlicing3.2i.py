text = input("Enter a string: ")

print("Original string:", text)
print("First 6 characters:", text[0:6])
print("Characters from index 3 to index 7:", text[3:7])
print("Characters from start to index 4:", text[:4])
print("Characters from index 3 to end:", text[3:])
print("Every 3rd character:", text[::3])
