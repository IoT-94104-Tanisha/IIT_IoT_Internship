text = "Good Morning!"

print(text.upper())  #convert to uppercase
print(text.lower())  #convert to lowercase
print(text.title())  #title case
print(text.capitalize()) #capitalize first letter
print(text.swapcase()) #swap upper to lower

print(text.isupper()) #check if uppercase
print(text.islower()) #check if lowercase
print(text.istitle()) #check if title case
